# Napisati program koji od korisnika traži unos polumjera kružnice te ispisuje njezinu površinu i opseg.




Polumjer_kruznice = float(input("Unesite polumjer kruznice: "))

pi = 3.14

Opseg = 2 * Polumjer_kruznice * pi

Površina = Polumjer_kruznice * Polumjer_kruznice * pi


print ("Opseg križnice je ", Opseg)
print ("Površina kružnice je ", Površina)






